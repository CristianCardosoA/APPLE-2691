import UIKit

struct Calculadora {
    func suma (x1: Double, x2: Double) -> Double {
        return x1 + x2
    }
        func resta(x1: Double, x2: Double) -> Double {
            return x1 - x2
    }
            func division(x1: Double, x2: Double) -> Double {
                return x1 / x2
    }
                func multiplicacion (x1: Double, x2: Double) -> Double {
                    return x1 * x2
    }
}
        
